#include "3-calc.h"

/**
 * op_add - Computes the sum of two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: the sum (a + b)
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - Computes the difference of two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: the difference (a - b)
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - Computes the product of two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: the product (a * b)
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - Computes the quotient of two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: the quotient (a / b)
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - Computes the remainder of the division of two integers
 *
 * @a: first integer
 * @b: second integer
 *
 * Return: the remainder (a % b)
 */
int op_mod(int a, int b)
{
return (a % b);
}
